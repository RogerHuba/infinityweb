// Helper functions to get skill data
export function getSkillPoints(professionSkillId: string): number {
  // Use the character builder's own skill points configuration
  const skillPoints = SKILL_POINTS_CONFIG[professionSkillId];
  if (skillPoints !== undefined) {
    return skillPoints;
  }

  // Fallback to CONSTANTS.js only if not defined in our config
  const constantsSkillId = SKILL_ID_MAPPING[professionSkillId];
  if (!constantsSkillId || !typedSkills[constantsSkillId]) {
    return 15; // Default fallback
  }
  return typedSkills[constantsSkillId].skillPoints || 15;
}

